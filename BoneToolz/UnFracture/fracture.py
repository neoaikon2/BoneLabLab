# Check to make sure this is just a .prefab file
def validateFilename(_fn):
	if (not '.prefab' in _fn):
		return False;
	if('_fixed' in _fn):
		return False;
	if('.meta' in _fn):
		return False;
	
	# File passes our checks
	return True;

# Function to traverse all directories and find the prefabs
def traverse(_path):
	# For each directory object
	for _dir in listdir(_path):
		# Get the FQFN
		_fn = join(_path, _dir)
		# See if it's a file or directory
		if (isfile(_fn)):
			# Validate that this is a .prefab file
			if(validateFilename(_fn)):
				print("Repairing: " + _fn)
				# Open the input file and the output file
				_f = open(_fn, "rt")
				_o = open(_fn[:len(_fn)-7] + "_fixed.prefab", "wt")
				
				# Read all the lines from the input file and close it				
				_data = _f.readlines()
				_f.close()
				
				# Go through each line
				for line in _data:
					# Go through each GUID in our mapping
					for guidIdx in range(len(keys)):
						# See if it contains an old GUID
						if keys[guidIdx] in line:
							# Replace it and break
							line = line.replace(keys[guidIdx], vals[guidIdx])
							continue
					# Write the output
					_o.write(line)
					
				# Close the file
				_o.close()
		else:
			# Else this is a directory so dive into it
			traverse(_fn)
